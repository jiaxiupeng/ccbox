export function BrandIcon({ brand, color, name, size = 36, className }: Props) {
  // Official BigModel (智谱 GLM) logo
  if (brand === "glm") {
    return (
      <img
        src={glmLogo}
        width={size}
        height={size}
        className={cn("rounded-lg object-cover", className)}
        alt="GLM"
        draggable={false}
      />
    );
  }

  const logo = brand ? LOGOS[brand] : undefined;

  if (logo) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={logo.color}
        className={cn(logo.invertDark && "dark:invert", className)}
        aria-label={brand}
      >
        <path d={logo.d} />
      </svg>
    );
  }

  // Custom fallback: colored badge with the first letter of the name.
  const glyph = (name?.trim()?.[0] ?? "C").toUpperCase();

  return (
    <div
      className={cn(
        "grid shrink-0 place-items-center rounded-lg font-semibold text-white",
        className,
      )}
      style={{ width: size, height: size, background: color || "#2563eb" }}
    >
      <span style={{ fontSize: size * 0.42, lineHeight: 1 }}>{glyph}</span>
    </div>
  );
}
